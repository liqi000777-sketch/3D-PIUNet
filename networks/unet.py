class ResBlockTimeEmbedClassConditioned(ResBlock):
    def __init__(
        self,
        channels,
        emb_channels,
        dropout,
        out_channels=None,
        use_conv=False,
        use_scale_shift_norm=False,
        dims=2,
        use_checkpoint=False,
        up=False,
        down=False,
        reduce_3dims=True,
        eeg_channels=0,
        eeg_embed_size=0,
    ):
        super().__init__(
            channels + eeg_embed_size,
            emb_channels,
            dropout,
            out_channels,
            use_conv,
            use_scale_shift_norm,
            dims,
            use_checkpoint,
            up,
            down,
            reduce_3dims,
        )
        self.class_conditioned = eeg_embed_size and eeg_channels
        if self.class_conditioned:
            self.linear_map_class = nn.Sequential(nn.SiLU(), nn.Linear(eeg_channels, eeg_embed_size))

    def forward(self, x, time_embed, c):
        if self.class_conditioned:
            emb_c = self.linear_map_class(c)
            while len(emb_c.shape) < len(x.shape):
                # Add a dimension of correct spatial size
                emb_c = emb_c[..., None]
                emb_c = emb_c.expand(*[-1] * (len(emb_c.shape) - 1), x.shape[len(emb_c.shape) - 1])
            x = th.cat([x, emb_c], dim=1)
        return super().forward(x, time_embed)



